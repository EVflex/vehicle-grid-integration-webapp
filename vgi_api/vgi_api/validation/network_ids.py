"""network_ids.py — which LV networks a user may select for detailed modelling.

HOW THIS FILE DIFFERS FROM THE ORIGINAL (v0) network_ids.py
-----------------------------------------------------------
The underlying network id lists are UNCHANGED from v0 (renamed to
_ALL_LV_NETWORKS_*). What changed (see CHANGES.md §16):

- Four networks (1106, 1107, 1142, 1143) host the demo scenario's lumped MV
  solar-farm and fast-charge-station demand. In v0, selecting any of them in
  `lv_list` crashed the whole simulation (the lumped MV load gets disabled
  when the network is expanded, and the DG/FCS placement then failed with
  `ValueError: x not in list`). They are now RESERVED_LV_NETWORKS — derived
  from run_dict0 so the set cannot drift from the scenario definition — and
  are filtered out of the selectable VALID_LV_NETWORKS_* lists served to the
  frontend and checked by the validator.
- MV_SOLAR_HOSTS / MV_FCS_HOSTS are exposed separately so /network-topology
  can tell the frontend map which nodes are solar vs fast-charging sites.
"""

from typing import List

from ..azureOptsXmpls import run_dict0


def _lumped_mv_asset_hosts(asset: str) -> List[int]:
    """LV networks that host one kind of default lumped-MV asset.

    The demo scenario (``azureOptsXmpls.run_dict0["dmnd_gen_data"]``) places the
    lumped MV solar / distributed-generator demand and the lumped MV
    fast-charge-station (FCS) demand onto specific LV networks, referenced by
    their id (e.g. ``dgs.mv = ["1106", "1142"]``, ``fcs.mv = ["1107", "1143"]``).
    That demand is attached to each network's *single lumped MV load*, which
    only exists while the network is modelled as a lump. Deriving the set here
    (rather than hard-coding it) keeps it automatically in step with run_dict0.
    """
    mv = run_dict0["dmnd_gen_data"].get(asset, {}).get("mv")
    if not isinstance(mv, list):
        return []
    return sorted({int(n) for n in mv})


# Hosts per asset kind, exposed separately so /network-topology can tell the
# frontend which map nodes are solar-farm sites vs fast-charging sites.
MV_SOLAR_HOSTS: List[int] = _lumped_mv_asset_hosts("dgs")
MV_FCS_HOSTS: List[int] = _lumped_mv_asset_hosts("fcs")

# CHANGE (2026-07-09): networks reserved for the lumped MV solar (DG) and
# fast-charge-station (FCS) demand. They must stay represented as a single
# lumped MV load so that demand is preserved, so they are deliberately withheld
# from the LV networks a user may model in full (expanding one would disable its
# lumped load and either drop that MV demand or crash the run). Excluded from
# the selectable lists below and rejected by validate_lv_list. See CHANGES.md.
RESERVED_LV_NETWORKS: List[int] = sorted(set(MV_SOLAR_HOSTS) | set(MV_FCS_HOSTS))

