# The networks a user may actually choose to model in full: every network in
# the feeder minus the RESERVED_LV_NETWORKS that must stay lumped for the MV
# solar / FCS demand. These filtered lists are what the `/lv-network` endpoint
# serves and what `validate_lv_list` checks against.
_RESERVED_SET = set(RESERVED_LV_NETWORKS)
VALID_LV_NETWORKS_URBAN: List[int] = [
    n for n in _ALL_LV_NETWORKS_URBAN if n not in _RESERVED_SET
]
